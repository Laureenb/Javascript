// Déclarez les variables suivantes :

//     La valeur est égal au chiffre le plus petit dans 
//     l'enssemble suivant : 7, 5, -12, 6, 32, 18, 14, -2
//     La valeur est égal au chiffre le plus grand dans 
//     l'enssemble suivant : -3, 9, 21, 36, 27, 54, 17, 35
//     La valeur est égal a l'addition des deux précédentes variables

var valeurMin = Math.min (7, 5, -12, 6, 32, 18, 14, -2);

var valeurMax = Math.max (-3, 9, 21, 36, 27, 54, 17, 35);

var total = valeurMin + valeurMax;

console.log(valeurMin + " " + "+" + valeurMax + " = " + total);
